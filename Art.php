      <h2>Les Art</h2>
      <p id="Art">Le vodun est une religion qui a réussi a érigé en véritable art sa liturgie. Il existe un courant vodun dans la peinture et la sculpture contemporaine en Afrique et à Haïti. Il s’agit d’un art vivant, coloré et mettant l’accent sur les éléments de la nature.

        <br>L’art vodun est souvent connu pour sa dimension religieuse. Assemblages d’éléments hétéroclites, ossements, pièces de bois, coquillages, plumes, ou encore mèches de cheveux, les sculptures Bocio (cadavre (cio) doté de pouvoirs (bo)) sont les principaux objets rituels du culte Vaudou. Placées dans l’intimité des maisons ou sur la place des villages, elles sont utilisées pour nuire ou pour protéger.
        
        <br>Parmi l’art vodun traditionnel, on retrouve aussi les asens qui sont des réalisations en fer forgé. Les asens sont des structures métalliques que l’on peut planter en terre et qui portent un petit plateau représentant une scène de la vie quotidienne.
        
        <br>il existe aussi un art contemporain vodun dans la peinture et les assemblage qui mélange des éléments du passé et de nouvelles réalités technologiques.
        
        <br>Le vodun est aussi riche en patrimoine musicale, costumes et en dances. A titre d’exemple, le patrimoine oral Gèlèdé a été inscrit sur par l’UNESCO sur la liste du patrimoine culturel immatériel de l’humanité.</p>
