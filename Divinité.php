  <hr>
  <h3 class="t7">Loko Atissou/ Loko : le gardien des sanctuaires, mais surtout la divinité de la végétation</h3>
  <p class="p8">Loko confère aux feuilles des arbres leurs propriétés magiques. Il s’agit d’une personnification des plantes. Il est identifié comme Saint Joseph. Sa compagne est Ayizan.

    <br>Divinité Hougan, médecin et initiateur, son symbole est l’asson. Il est la divinité des nouvelles, identifié aussi à Saint Gabriel. C’est lui qui donne l’asson. C’est un guérisseur dont la couleur est le jaune ou parfois le rouge. On lui donne de l’alcool à boire et du bœuf à manger.
    
    <br> adeptes fument la pipe et tiennent une canne à la main. Les offrandes à son intention sont déposées dans des sacs accrochés aux branches d’un arbre sacré.</p>
    <img src="media/loko-atissou-3624-244x300.jpg" class="im6" width="150">
