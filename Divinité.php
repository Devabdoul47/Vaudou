      <h2>Les divinités</h2>
      <br>
      <br><br>
<p class="p1">Il existe une multitude de divinités vaudoues au Benin, représentant chacune des éléments clés de l’univers :</p>
<h3 class="t1">HEVIOSSO (Feu)</h3>
<h3 class="t1">AYIDOHWEDO (Air)</h3>
<h3 class="t1">DAN (Eau)</h3>
<h3 class="t1">SAKPATA (La Terre)</h3>
<p class="p2">Les Vaudous seraient donc, des médiateurs entre Dieu et les hommes. Ils obtiennent des faveurs, mais sont également les exécuteurs des vengeances divines.</p>
<hr>
<h3 class="t2">Hevioso/ Xebiosso/Shango/chango : divinité du feu et de la foudre</h3>
<p class="p3">Frère de Gou, il est le dieu du feu de la foudre ainsi que le dieu de la justice. Il a trois épouses : Oba, Oya et Oshun. Shango est représenté avec une double hache, symbole de la foudre, sur la tête. Il est également représenté avec six yeux, et parfois trois têtes. <br> symbole est une hache à double tranchants.
  
 <br> Ses animaux sont le bélier, le cheval, la colombe et le faisan.
  
  <br> adeptes de Shango portent des vêtements de couleur rouge et blanc.</p>
