    <h3 class="t12">Itcha Nla/Orisha/Orisa</h3>
    <p class="p13">Au départ, une ethnie formant la mosaïque culturelle du Bénin qui appartient au groupe linguistique des Yoruba.

      <br> s’agit d’une divinité majeure dans le vaudou. Itcha Nla est le nom donné au vaudou en langue nago ou yoruba. Il s’agit aussi du terme qui désigne l’ensemble des mythes, des chants, des histoires et des légendes yoruba.
      
     <br>Le concept d’Orisha fait appel à des notions spirituelles complexes. Les Orisha ne sont pas des forces naturelles à l’état brut comme plusieurs autres divinités vaudoues, mais plutôt des êtres humains divinisés qui vivaient jadis sur terre et qui ont créé avec les forces naturelles une chaîne de relations entre le divin et le terrestre au bénéfice des hommes. Pour remercier les Orisha, ceux-ci leur rendent hommage en faisant des offrandes et des sacrifices nécessaires au maintien de leur pouvoir. Les Orisha sont représentés par un caméléon qui s’adapte à toute situation.</p>
