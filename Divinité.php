<h3 class="t4">Damballa Woedo : divinité de la fécondité, de la bonté et de la connaissance</h3>
<p class="p5">On le trouve souvent sous d’autres noms comme Damballa ou Saint Patrick.

  <br> est l’époux d’Ayida Wedo (Aïdo woedo), divinité du ciel et des tempêtes. Il offre des connaissances, du savoir et des conseils judicieux. Il vit près des rivières et des sources d’eau. Ses attributs sont la couleur blanche, les œufs, la farine, le sirop de maïs et les poulets blancs.
  
  <br>Les symboles de Damballa Woedo sont le serpent, l’arc-en-ciel et l’éclair.</p>
  <img src="media/aido-woedo.jpg" class="im3" width="130">
  <img src="media/aido-3620-238x300.jpg" class="im3" width="150">
  <hr>
  <h3 class="t5">Oxumare/Dan /Tovodoun /Nensuxué : la divinité de l’eau, de la prospérité et du bien-être</h3>
  <p class="p6">Dan désigne le serpent, plus particulièrement le python, un animal sacré qu’on ne doit pas tuer. Il est soit considéré comme l’assistant de Shango ou l’une des épouses de Shango. Il correspond à Saint Patrick ou Saint Bartholomew dans le vaudou chrétien.

    <br> a assisté à la création et soutient l’univers. Il est parfois dessiné comme étant un serpent, mais peut prendre diverses formes selon les communautés. Il règne non seulement sur la santé et la vie, mais aussi sur la maladie et la mort. Il est responsable de la communication entre les deux sphères, supérieure et inférieure, du cosmos.
    
    <br> Vaudou Dan existe alors sous plusieurs déclinaisons : Tohossou, Todan, Aguédjidan et Attindan.
    
    <br>Il est également le maître de tout ce qui est allongé, y compris le cordon ombilical. Sa couleur est le jaune.</p>
  <img src="media/oxumare-243x300.jpg" class="im4" width="150">
  <hr>
  <h3 class="t6">Sakpata :</h3>
  <p class="p7">Sakpata est le Dieu de la terre ou de la variole, plus largement des maladies éruptives ou contagieuses. Sakpata est aussi nommé Ayinon, le propriétaire de la terre. Ce statut lui confère la responsabilité de nourrir les hommes en leur offrant des produits agricoles. Dieu de l’abondance, il est imploré pour de bonnes moissons. Il correspond à Saint Lazare.

    <br> symbole est la jarre trouée qui rappelle les boutons et les taches indélébiles laissés par les maladies qui lui sont associées, sur le corps des personnes qui en sont atteintes. Les adeptes du Sakpata portent les couleurs rouge, noir et blanc. Sakpata nourrit l’homme en lui donnant le maïs, le mil et toutes les autres céréales.</p>
