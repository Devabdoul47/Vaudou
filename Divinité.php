    <h3 class="t10">Lègba/Elegba/Esu</h3>
    <p class="p11">D’après la légende, Lègba ou Elegba est soit le dernier fils ou le fils ainé de Mawu et de Lisa.

      <br> est assimilé à Saint Pierre, qui détient les clefs du Paradis et de l’Enfer selon le vaudou haïtien, ainsi qu’à Saint Antoine et Saint Lazare.
      
      <br> le vaudou africain, la notion de paradis et d’enfer n’existe pas. Par conséquent, Lègba (Eshu) est le dieu le plus important, car il est le dieu des croisements et de la réflexion. Son rôle d’intermédiaire est secondaire. Il forme avec la divinité Fa (ou Ifa) un couple porteur de la pédagogie de cette culture.
      
      <br> note deux théories sur la légende de Legba. Selon la première légende, l’être suprême Mawu a convoqué les Vaudous pour les envoyer sur terre. Lègba s’est alors présenté le premier, sans prendre le temps ni la politesse d’apporter un cadeau. Il ne portait qu’une simple plume sur la tête. Mawu, fâchée, l’a renvoyé. Dépité, Lègba est descendu sur terre sans instructions ni but véritable. Il a erré dans des lieux inconnus, ne sachant que faire. Comme il connaissait les langues des deux mondes, soit les langues divine et humaine, il a mis à profit cette compétence en devenant le messager entre les mondes divin et profane.
      
      <br>La seconde légende indique que Lègba pourrait être non pas le dernier né, mais au contraire, le premier né « préféré » de Mawu, un enfant malformé né longtemps avant la série d’enfants normaux.
      
      <br> symboles sont la croix, les clés, le bâton de marche, les béquilles et les miroirs. Ses couleurs sont le blanc et le rouge.</p>
