    <h3 class="t11">Fâ/IFa/Afa : la divinité de la providence qui symbolise le destin de chaque être humain</h3>
    <p class="p12">Fa est là où les esprits vaudous viennent parler aux humains.

      <br> la tradition Fon du vaudou dahoméen, Mawu-Lisa, le créateur double, a envoyé deux messagers sur la Terre pour informer les êtres humains qu’ils auraient leur propre fa, parole ou écriture sacrée, qui serait leur destin. Chaque jour, Mawu-Lisa donnait cette écriture à son assistante, Lègba. Chaque jour, Lègba savait qui allait naître ou mourir, les dangers auxquels chaque personne aurait à faire face et les personnes à qui la fortune sourirait. Lègba apportait à chaque individu son fa. On questionne Fa à l’aide d’un chapelet nommé Odu.
      
      <br> est aussi un oracle originaire du Nigeria. La ville Ifè au Nigeria est le sanctuaire du fâ.
      
      <br> 16 Dou ou ODU (symboles) du fâ sont les suivants.</p>
