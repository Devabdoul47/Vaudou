  <img src="media/chang0-3621-235x300.jpg" class="im1" width="150">
  <img src="media/xebiosso-3622-238x300.jpg" class="im1" width="150">
<hr>
<h3 class="t3">Ogou : divinité du fer et de la guerre</h3>
<p class="p4">On le trouve souvent sous d’autres noms comme Gu, Ogun, Ogu, Ogum, Ogou, Ogoun, Ogorin, Ogu-badagri, Saint Jacques ou Saint Sébastien.

  <br>Il est le dieu du fer, le dieu de la guerre, le chasseur des pirates. Il est représenté par un amas de fer. Ogou est le dieu des forgerons et de tous ceux qui utilisent ce métal : agriculteurs, chasseurs, guerriers, bouchers, barbiers, menuisiers charpentiers et sculpteurs. Il s’agit du frère de Shango. Ses attributs sont les couleurs rouge et bleu, le sabre, le coq rouge, le rhum, le tabac, les haricots rouges et le riz, l’igname, les coqs rouges et les taureaux rouges.
  
  <br> représente la puissance et la liberté.</p>
