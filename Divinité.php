    <h3 class="t8">Ayizan/ Ayizan Avlékété/ Ayizan DODO : la divinité du commerce et des marchands</h3>
    <p class="p9">Épouse de Legba et compagne de Loko Atissou, elle est la protectrice des marchés et la patronne des marchands. Cette divinité est reconnue pour sa bienfaisance. Elle apporte la guérison par les plantes et accorde la protection et sa puissance aux initiés. Ayizan, qui est représentée par Sainte Claire, protège contre les mauvais sorts. Elle ne boit pas d’alcool. Ses symboles sont les feuilles de palmier et les bougies.</p>
    <img src="media/aizanvelekete-3627-238x300.jpg" class="im7" width="150">
