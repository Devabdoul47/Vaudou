    <h3 class="t9">Avléketé/Azaka/nommé aussi Avlékété-Tins ou vaudou Avlékété Gbimgbo, est une divinité de l’eau</h3>
    <p class="p10">Avlékété était la divinité suprême la plus adorée de la cité de Godomey. Avlékété est un Vaudou qui commande les énergies qui séparent l’eau de la terre. Avlékété est malicieux, farceur et en perpétuel mouvement. Il forme une triade avec Agbé et Houtous, tous des fils de Mawu-Lisa. Avlékété est le génie qui contrôle le mouvement des vagues. Le temple principal de Hou se trouve à Ouidah. On prétend qu’il a la force de faire exécuter tous vos désirs, peu importe qu’ils soient en lien avec l’amour, la carrière, la richesse, la vengeance, la santé ou la justice.

      <br> symbole est le crapaud, respectueusement appelé tohè (oiseau d’eau). Pour ses adeptes, il symbolise la fécondité, l’abondance et la présence permanente de l’eau.</p>
