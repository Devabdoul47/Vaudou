   <h2>La région vaudou</h2>
      <p class="pregion">Le dogme vodun considère Mawu-Lisa, couple siamois formé de la lune et du soleil et représentant par sa dualité l’équilibre de l’univers comme la divinité suprême. Mawu représente le principe féminin, sa figure est associée au froid et à la fécondité tandis que lisa, son frère, est une icône masculine qui incarne la force. Le couple siamois créa la terre avec l’aide de Dan, le serpent cosmique et engendra les quatorze divinités du panthéon vodun.
       vodun est batit sur une cosmogonie hiérarchisé et rationnelle lui donnant le statut de religion officielle au Bénin et dans d’autres pays. Les entités du panthéon sont des intermédiaires – des messagers – entre l’humain et le divin. Aux dieux s’ajoutent les ancêtres soit ethniques ou familiales et les éléments de la nature pour composer un tout complexe.
        vodun est une très vieille religion vernaculaire qui prêche un continuum unie entre les générations par le culte des ancêtres mais aussi une symbiose avec la terre génitrice, protectrice et féconde.
