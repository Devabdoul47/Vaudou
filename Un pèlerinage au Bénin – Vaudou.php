      <h2>Le pèlerinage au Bénin</h2>
      <img src="media/carte-afrique.jpg"  width="300px" id="pelerin1">
      <p id="pèlerin">Le Bénin est situé en Afrique de l’Ouest dans la zone tropicale entre l’équateur et le tropique du Cancer (entre les parallèles 6°30’ et 12°30’ de latitude nord et les méridiens 1° et 30°40’ de longitude est).Il est limité :</p>
      <ul>
        <li>au nord par la République du Niger sur 277 km avec 120 km délimités par le fleuve Niger ;</li>
        <li>au nord-ouest par le Burkina Faso (sur 386 km) ;</li>
        <li>à l’ouest par le Togo (sur 651 km),</li>
        <li>à l’est par le Nigeria (sur 809 km) ;</li>
        <li>et au sud par l’océan Atlantique (sur 121 km).</li>
      </ul>
      <p id="pèlerin2">Du nord au sud, il s’étend sur 700 Km; la largeur varie de 125 Km (le long de la côte) à 325 Km (à la latitude Tanguiéta-Ségbana).Le pays est assez plat. On distingue cinq (05) régions naturelles :</p>
      <ul>
        <li>Une bande côtière, basse et sablonneuse, limitée par des lagunes (cocoteraies) ;</li>
        <li>Une plaine centrale, vallonnée et monotone, qui s’élève progressivement de 200 à 400 m du sud vers le nord aux environs de Nikki puis s’abaisse jusqu’à la vallée du Niger et au bassin de Kandi. Le bassin de Kandi au nord-est se présente comme une plaine drainée par la rivière Sota et ses affluents qui coulent dans des vallons très évasés ;</li>
        <li>La chaîne de l’Atacora au nord-ouest, où se situe le point culminant du pays, le mont Aledjo (658m);</li>
        <li>La vaste plaine du Gourma à l’extrême nord-ouest, entre l’Atacora et la frontière avec le Burkina Faso et le Togo.</li>
        <li>La savane humide occupe la majeure partie du pays. Quelques îlots de forêt primaire subsistent dans le Sud et le Centre. Des cultures, des zones marécageuses et l’immense palmeraie du Bas-Bénin occupent le reste du territoire.</li>
      </ul>
      <br>
      <h3 id="climat">LE CLIMAT</h3>
      <p>Le Bénin possède deux (02) types de climat :

       <br> Au sud, un climat équatorial avec une forte humidité. Alternance de saisons sèches (de novembre à mars et de mi-juillet à mi-septembre) et de saisons des pluies (d’avril à mi-juillet et de mi-septembre à octobre).
        
       <br> Au centre et au nord, un climat tropical. Une saison sèche de novembre à avril et une saison des pluies de juin à septembre.
        
        <br>L’harmattan, vent chaud et sec en provenance du Sahara, souffle sur toute l’étendue du territoire pendant la saison sèche.
        
        <br>À Cotonou, les températures ont une faible amplitude : les maximales varient de 28 à 32°C et les minimales de 23 à 26°C. La pluviométrie annuelle dans cette ville est de 1245 mm.</p>
        <h3>Le relief du Bénin est peu accidenté et comprend :</h3>
        <ul>
          <li>une région côtière, basse et sablonneuse limitée par des lagunes;</li>
          <li>un plateau d’argile ferrugineux ;</li>
          <li>un plateau silico-argileux, parsemé de quelques sous-bois;</li>
          <li>au nord-ouest, le massif de l’Atacora (800 mètres);</li>
          <li>au nord-est, les plaines du Niger, silico-argileuses très fertiles.</li>
        </ul>
        <h3>HYDROGRAPHIE DU BÉNIN</h3>
        <p>Plusieurs cours d’eau traversent le pays.<br> bassin béninois du fleuve Niger qui comprend les rivières</p>
        <ul>
          <li>le Mékrou : 480 km;</li>
          <li>l’Alibori : 427 km;</li>
          <li>la Sota: 254 km;</li>
          <li>et la Pendjari.</li>
        </ul>
        <h3>Le bassin de la Volta qui comprend :</h3>
        <ul>
          <li>le Pendjari (420 km) ;</li>
          <li>le Perma.</li>
        </ul>
        <h3>Le bassin du Mono-Couffo qui comprend :</h3>
        <ul>
          <li>le Couffo : fleuve de 190 km (dont 170km au Bénin), et qui se jette dans la lagune</li>
          <li>le Mono : avec ses 500 km, il sert de frontière entre le Bénin et le Togo sur son cours inférieur et se jette ensuite dans l’océan Atlantique.</li>
        </ul>
        <p>Le bassin de l’Ouémé : avec ses 608 km, l’Ouémé se jette dans la lagune Nokoué et utilise les chenaux de Lagos et de Cotonou pour communiquer avec l’océan Atlantique.On compte aussi plusieurs plans d’eau dans le sud à savoir:</p>
        <ul>
          <li>le lac Nokoué : 138 km2;</li>
          <li>le lac Ahémé : 78 Km2;</li>
          <li>et la lagune de Porto-Novo : 35 km2.</li>
        </ul>
        <h3>LA VÉGÉTATION ET LA FAUNE</h3>
          <p>Trois (03) types de végétation caractérisent le Bénin:</p>
          <ul>
            <li>la savane arborée dans les régions soudanaises du Nord ;</li>
            <li>la savane au Centre avec des essences comme l’Acajou, l’Iroko, la Samba ;</li>
            <li>et la forêt au Sud et au Moyen Bénin</li>
          </ul>
          <p>Quant à la faune, on distingue deux (02) parcs nationaux au Nord:</p>
          <ul>
            <li>le parc Pendjari : 275.000 ha;</li>
            <li>le parc «W»: 502.000 ha.</li>
          </ul>
          <p>On y rencontre des éléphants, des buffles, des hippopotames, des lions, des guépards, des caïmans, des antilopes, des oiseaux, des singes, des reptiles, des léopards, des insectes, etc.</p>
